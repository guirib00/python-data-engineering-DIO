# Definindo duas variáveis com valores numéricos
a = 10
b = 5

# Soma
soma = a + b
print(f'Soma: {a} + {b} = {soma}') # Imprimindo o resultado da soma utilizando f-string para mostrar variaveis em uma string

# Subtração
subtracao = a - b
print(f'Subtração: {a} - {b} = {subtracao}')

# Multiplicação
multiplicacao = a * b
print(f'Multiplicação: {a} * {b} = {multiplicacao}')

# Divisão
divisao = a / b
print(f'Divisão: {a} / {b} = {divisao}')

# Divisão inteira
divisao_inteira = a // b
print(f'Divisão inteira: {a} // {b} = {divisao_inteira}')

# Módulo (resto da divisão)
modulo = a % b
print(f'Módulo: {a} % {b} = {modulo}')

# Exponenciação
exponenciacao = a ** b
print(f'Exponenciação: {a} ** {b} = {exponenciacao}')