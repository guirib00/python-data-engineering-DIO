# Lista de frutas
frutas = ['maçã', 'banana', 'laranja', 'uva']

# Verifica se 'maçã' está na lista de frutas
if 'maçã' in frutas:
    print('A maçã está na lista de frutas.')

# Verifica se 'abacaxi' não está na lista de frutas
if 'abacaxi' not in frutas:
    print('O abacaxi não está na lista de frutas.')

# String de exemplo
