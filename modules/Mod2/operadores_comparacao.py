# Definindo Variaveis
a = 5
b = 3

# Verifica se a é igual a b "a == b"
print(f"{a} == {b}: {a == b}")

# Verifica se a é diferente de b "a != b"
print(f"{a} != {b}: {a != b}")

# Verifica se a é maior que b "a > b"
print(f"{a} > {b}: {a > b}")

# Verifica se a é menor que b "a < b"
print(f"{a} < {b}: {a < b}")

# Verifica se a é maior ou igual a b "a >= b"
print(f"{a} >= {b}: {a >= b}")

# Verifica se a é menor ou igual a b "a <= b"
print(f"{a} <= {b}: {a <= b}")